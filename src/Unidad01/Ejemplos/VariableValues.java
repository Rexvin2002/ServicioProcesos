public class VariableValues {

    public static void main(String[] args) {
        // Declaración de las variables con los valores requeridos
        int a = 200;
        int b = 120;
        int c = 3000;
        int d = 32777;
        float e = 3.2f;
        long f = -2147483638L;
        boolean g = true;

        // Mostrando los valores de las variables
        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);
        System.out.println("d: " + d);
        System.out.println("e: " + e);
        System.out.println("f: " + f);
        System.out.println("g: " + g);

        // Crear un número con valor 120 y sumarle 10
        int h = 120;
        h += 10;
        System.out.println("h (120 + 10): " + h);

        // Crear un char con valor 7 (como carácter)
        char i = 7;
        System.out.println("i (char con valor 7): " + i);
    }
}

